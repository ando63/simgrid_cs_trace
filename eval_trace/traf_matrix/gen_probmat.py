def random_split(seed, num_parts):
    # シード値を設定
    np.random.seed(seed)
    
    # 0から1までのランダムな値を生成
    random_values = np.random.rand(num_parts - 1)
    
    # 生成したランダムな値をソート
    random_values.sort()
    
    # 最初の値を0, 最後の値を1に追加して、区間を作成
    intervals = np.concatenate([[0], random_values, [1]])
    
    # 各区間の幅を計算して1を8分割
    splits = np.diff(intervals)
    
    return splits

def generate_probability_matrix(size, seed):
    rs = np.random.RandomState(seed)
    matrix = rs.rand(size, size)
    # normalize for each row
    matrix /= matrix.sum(axis=1, keepdims=True)
    
    return matrix

